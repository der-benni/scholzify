async function loader() {
  const PATH = "/api/home-page";
  const BASE_URL = "http://localhost:1337";
  const API_URL = new URL(PATH, BASE_URL);

  const response = await fetch(API_URL.href);
  const data = await response.json();

  return { ...data.data }
}

